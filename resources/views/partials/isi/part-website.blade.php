<script>
    document.addEventListener('DOMContentLoaded', function () {
    const webButtons = document.querySelectorAll('.web-box'); // Semua tombol
    const webSections = document.querySelectorAll('.web-section'); // Semua section

    // Atur default untuk menampilkan "UMUM"
    const defaultSection = 'semua-web'; // ID dari section default
    webSections.forEach((section) => {
        if (section.id === defaultSection) {
            section.classList.remove('hidden'); // Tampilkan section default
        } else {
            section.classList.add('hidden'); // Sembunyikan section lainnya
        }
    });
    webButtons.forEach((button) => {
        if (button.getAttribute('data-web') === defaultSection) {
            button.classList.add('border-[#EA891B]', 'text-[#EA891B]'); // Highlight tombol default
        } else {
            button.classList.remove('border-[#EA891B]', 'text-[#EA891B]');
        }
    });

    // Event listener untuk tombol
    webButtons.forEach((button) => {
        button.addEventListener('click', function () {
            const selectedWeb = this.getAttribute('data-web'); // Ambil data-web dari tombol yang diklik

            // Tampilkan hanya section yang sesuai
            webSections.forEach((section) => {
                if (section.id === selectedWeb) {
                    section.classList.remove('hidden'); // Tampilkan section yang sesuai
                } else {
                    section.classList.add('hidden'); // Sembunyikan section lainnya
                }
            });

            // Highlight tombol aktif
            webButtons.forEach((btn) => btn.classList.remove('border-[#EA891B]', 'text-[#EA891B]')); // Reset tombol lainnya
            this.classList.add('border-[#EA891B]', 'text-[#EA891B]'); // Highlight tombol yang diklik
        });
    });
});

</script>
